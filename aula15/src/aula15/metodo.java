// como usar método

//PADRÃO DE CRIAÇÃO DE ARQUIVO - JAVA
// 1 - package
package aula15;

// 2 - classe (nome do arquivo)
public class metodo {

	// 3 - sempre criar os nossos métodos após a classe
	public static void corFavorita() {
		// obrigatóriamente parenteses
		// possui escopo
		// linhas de código
		
		System.out.println("verde");
	
	}

	// 4 - cria o metodo main
	public static void printNome(String[] args) {
		// para chamar um método: vou escrever o nome dele, sem esquecer os parenteses
		
		corFavorita();

	
	}

}
